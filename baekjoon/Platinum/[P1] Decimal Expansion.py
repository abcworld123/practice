from decimal import Decimal

A = [2, 4, 0, 2, 4, 0]
B = [0, 0, 1, 1, 2, 1]
C = [0, 9, 8, 9, 0, 1]
ans = []

input()
for x in map(int, input().split()):
    r = ((24 * Decimal(x) + 1).sqrt() + 1) / 12
    l = int(r)
    if l == r: ans.append(1); continue
    i, j = -1, x - 6 * l * l + l
    while j > 0: i += 1; j -= B[i] + l * A[i]
    ans.append(C[i])

print(' '.join(map(str, ans)))


