def make(rep, B):
    dic = {1: {0, 1}}
    ans = ['+![]', '+!![]'] + [''] * (B - 1)
    for i in range(2, rep + 1):
        dic[i] = set()
        for j in range(1, i):
            # print(f'{j} / {i}')
            for a in dic[i - j]:
                for b in dic[j]:
                    add, sub, mul = a + b, a - b, a * b

                    if 2 <= add <= B and not ans[add]:
                        dic[i].add(add)
                        l, r = ans[a], ans[b]
                        if l[:2] == '+!': l = l[1:]
                        if a <= 1 or b <= 1: s = f'{l}{r}'
                        else: s = f"{l}-+-{r if r[0] != '+' else r[1:]}"
                        ans[add] = s

                    if 2 <= sub <= B and not ans[sub]:
                        dic[i].add(sub)
                        s = f"{ans[a]}-{ans[b] if ans[b][:2] != '+!' else ans[b][1:]}"
                        ans[sub] = s

                    if mul <= B and not ans[mul]:
                        dic[i].add(mul)
                        ans[mul] = f'[{ans[a]}]*[{ans[b]}]'

                    if b > 1:
                        pow = a ** b
                        if pow <= B and not ans[pow]:
                            dic[i].add(pow)
                            ans[pow] = f'[{ans[a]}]**[{ans[b]}]'

                    if b >= 0:
                        cat = int(f'{a}{b}')
                        if 2 <= cat <= B and not ans[cat]:
                            dic[i].add(cat)
                            ans[cat] = f'[{ans[a]}]+[{ans[b]}]'
    return dic, ans

